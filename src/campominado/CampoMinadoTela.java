    public JPanel gerarNivel() {

        JPanel painel = new JPanel(new GridLayout());
        painel.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 0, 0)));

        Random rdn = new Random();

        Collections.shuffle(Arrays.asList(valores));

        for (int i = 0; i < 4; i++) {

            BotaoJogo btn = new BotaoJogo();
            btn.setTipoBotao(valores[i]);
            btn.setSize(20, 10);
            //btn.setLocation(x += 15, y += 15);
            btn.setText("Click");
            btn.setMargin(new Insets(0, 10, 0, 20));

            //tela.getContentPane().add(btn);           
            painel.add(btn);
        }
        //painel.setName(Integer.toString(nivelDoPainel));
        //nivelDoPainel++;

        return painel;
    }

    public void bloquearNivel(JPanel pnNivel) {

        Component[] objetos = pnNivel.getComponents();

        for (Component obj : objetos) {
            BotaoJogo btn = (BotaoJogo) obj;
            btn.setEnabled(false);
        }

    }

    public void liberarNivel(JPanel pnNivel) {

        Component[] objetos = pnNivel.getComponents();

        for (Component obj : objetos) {
            ((BotaoJogo) obj).setEnabled(true);
        }
    }

    public void fimDeJogo() {

        Component[] paineis = pnCampo.getComponents();
        for (Component painel : paineis) {
            bloquearNivel((JPanel) painel);
        }
    }

