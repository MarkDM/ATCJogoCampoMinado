    public void addAcaoBotao(JPanel painelAlvo) {
        Component[] objetos = painelAlvo.getComponents();

        for (Component obj : objetos) {
            BotaoJogo btn = (BotaoJogo) obj;
            btn.addActionListener(new java.awt.event.ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    JOptionPane.showMessageDialog(null, btn.getTipoBotao());

                    switch (btn.getTipoBotao()) {
                        case "BOMBA":
                            CampoMinadoTela.vidas--;
                            if (CampoMinadoTela.vidas <= 0) {
                                fimDeJogo();
                                JOptionPane.showMessageDialog(null, "Game Over!!!");

//                                int resposta = JOptionPane.showConfirmDialog(null, "Deseja tentar novamente?", "Game Over!!!", JOptionPane.YES_NO_OPTION);
//                                if (resposta == JOptionPane.YES_OPTION) {
//                                    restartJogo();
//                                } else {
//                                    fimDeJogo();
//                                }
                            }
                            btn.setEnabled(false);
                            break;
                        case "SETA":
                            btn.setEnabled(false);
                            int nivelAtual = Integer.parseInt(painelAlvo.getName());

                            switch (nivelAtual) {
                                case 1:
                                    JOptionPane.showMessageDialog(null, "You Win!!!");
                                    break;
                                case 2:
                                    liberarNivel(getPanelNivel1());
                                    break;
                                case 3:
                                    liberarNivel(getPanelNivel2());
                                    break;
                                case 4:
                                    liberarNivel(getPanelNivel3());
                                    break;
                            }

                            break;
                        case "INTERROGAÇÃO":
                            btn.setEnabled(false);
                            CampoMinadoTela.vidas++;
                            break;
                        default:
                            JOptionPane.showMessageDialog(null, "Opção: " + btn.getTipoBotao() + "Não é válida!");
                    }

                }
            });
        }
    }

